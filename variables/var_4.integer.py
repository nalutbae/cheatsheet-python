# Example of using operators for integer variables

x = 10
y = 20
z = 30

print("x =", x)
print("y =", y)

print("x + y =", x + y)
print("x - y =", x - y)
print("x * y =", x * y)
print("x / y =", x / y)
print("x // y =", x // y)
print("x % y =", x % y)
print("x ** y =", x ** y)
print("x > y =", x > y)
print("x < y =", x < y)
print("x == y =", x == y)
print("x != y =", x != y)
print("x >= y =", x >= y)
print("x <= y =", x <= y)
print("x and y =", x and y)
print("x or y =", x or y)
print("not x =", not x)
print("not y =", not y)
print("x is y =", x is y)
print("x is not y =", x is not y)

compareMultiple = x < y < z 
print("x < y < z =", compareMultiple)